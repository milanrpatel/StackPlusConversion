    private void prefixButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prefixButtonActionPerformed
        //prefix
        String get = prefixTextBox.getText();
        if (get.length() == 0) {

            JOptionPane.showMessageDialog(this, "Please, Enter Vailid Input.");
        } else {

            String putPost = Conversion.prefixToPostfix(get);
            String putIn = Conversion.postfixToInfix(putPost);
            postfixTextBox.setText(putPost);
            infixTextBox.setText(putIn);
        }
    }//GEN-LAST:event_prefixButtonActionPerformed

