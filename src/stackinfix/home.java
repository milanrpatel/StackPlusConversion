    private void popButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popButtonActionPerformed
        //pop
        if (stack.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Stack Is Underflow.");
        } else {
            if (top == 1) {
                top--;
                s0.setText(null);
                sp = stack.pop();
                lastPop(sp);
            } else if (top == 2) {
                top--;
                s1.setText(null);
                sp = stack.pop();
                lastPop(sp);
            } else if (top == 3) {
                top--;
                s2.setText(null);
                sp = stack.pop();
                lastPop(sp);
            } else if (top == 4) {
                top--;
                s3.setText(null);
                sp = stack.pop();
                lastPop(sp);
            } else if (top == 5) {
                top--;
                s4.setText(null);
                sp = stack.pop();
                lastPop(sp);
            } else if (top == 6) {
                top--;
                s5.setText(null);
                sp = stack.pop();
                lastPop(sp);
            }
        }
        topP(top - 1);
    }//GEN-LAST:event_popButtonActionPerformed

    private void pushButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pushButtonActionPerformed
        //push
        String s = pushTextBox.getText();
        if (top == 6) {

            JOptionPane.showMessageDialog(this, "Stack Is Overflow.");
        } else if (s.length() == 0) {
            JOptionPane.showMessageDialog(this, "Please, Enter Value.");
        
        }else{
            if(Character.isDigit(s.charAt(0)) || StackInfix.isOperator(s.charAt(0))|| (!(s.length()>1) && !Character.isDigit(s.charAt(0)))){

                if (top == 6) {

                    JOptionPane.showMessageDialog(this, "Stack Is Overflow.");
                } else if (top == 0) {
                    s0.setText(s);
                    stack.push(s);
                    top++;
                } else if (top == 1) {
                    s1.setText(s);
                    stack.push(s);
                    top++;
                } else if (top == 2) {
                    s2.setText(s);
                    stack.push(s);
                    top++;
                } else if (top == 3) {
                    s3.setText(s);
                    stack.push(s);
                    top++;
                } else if (top == 4) {
                    s4.setText(s);
                    stack.push(s);
                    top++;
                } else if (top == 5) {
                    s5.setText(s);
                    stack.push(s);
                    top++;
                }
            }else{
                JOptionPane.showMessageDialog(this, "Please, Enter Value.");
            }
        }
        pushTextBox.setText(null);
        topP(top - 1);
    }//GEN-LAST:event_pushButtonActionPerformed

